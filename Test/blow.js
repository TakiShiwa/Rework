    // JavaScript
    function togglePopup() {
      const popup = document.getElementById("popupContainer");
      popup.style.display = popup.style.display === "none" ? "block" : "none";
    }

    function hidePopup() {
      const popup = document.getElementById("popupContainer");
      popup.style.display = "none";
    }
